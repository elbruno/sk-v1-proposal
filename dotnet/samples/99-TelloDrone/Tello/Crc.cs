        public static int UCRC(byte[] bytes, int len, int poly)
        {
            int j = 0;
            int i = poly;
            poly = j;
            while (len != 0)
            {
                j = bytes[poly] ^ i;
                i = j;
                if (j < 0)
                {
                    i = j + 256;
                }
                i = uCRCTable[i];
                poly += 1;
                len -= 1;
            }
            return i;
        }

        public static void CalcUCRC(byte[] bytes, int len)
